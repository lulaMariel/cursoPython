# Operadores asignación: =, +=, -=, *=, /=, %=, //=, **=
# Expecto el "=", el resto permite modificar el valor de la variable al momento de ejecutar la operación

# = 

print("\n")
x = 10
print(x) # 10
print("\n")

# += y -=

x += 5
print(x) # 15

x -= 5
print(x) # 10
print("\n")

# *= y /=

x *= 2
print(x) # 20

x /= 2
print(x) # 10
print("\n")

# **=, //=, %=

x **= 3
print(x) # 1000

x //= 2
print(x) # 500

x %= 2
print(x) # 0