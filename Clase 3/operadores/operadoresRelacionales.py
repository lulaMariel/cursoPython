# Operaciones relacionales: Igual (==), distinto (!=), mayor (>), menor(<), mayor o igual (>=), menor o igual (<=)

# Igual

print("\n")
print("mano" == "mano") # True
print(4 == 2 + 2) # True
print(4 == "4") # False

# Distinto
print(4 != "4") # True
print(9 != 4 + 5) # False
print("\n")

# Mayor
print(12 > 3) # True
print("\n")

# Menor
num1 = 12 
num2 = 15

print(num1 < num2) # True