# Operar con strings: Concatenar (+) y multiplicar (*)

# Concatenación: Une los strings

print("\n")
print("Hola " + "mundo!") # Hola mundo!

a = "¡Hola "
b = "mundo!"
print(a + b) # ¡Hola mundo!
print("\n")

# Multiplicación: Repite el string

print("hola " * 3)
print("\n")

risa = "ja"
print(risa * 3) # jajaja