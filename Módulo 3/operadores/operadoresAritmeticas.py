# Operaciones aritmeticas: Suma (+), resta (-), multiplicación (*), potenciación (**), división cociente (/), división parte entera (//), divisón resto (%)

# Suma, resta, multiplicación: Funcionan igual, solo hay que cambiar el símbolo

print("\n")
print(8 + 8)
print("\n")

a = 8
b = 3.5

print(a - b)
print("\n")

c = 8
print(c * 10)
print("\n")

# Potenciación: El primer elemento es la base y el segundo elemento es el exponente

print(5 ** 2)
print("\n")

a = 5
b = 2
print(a ** b)
print("\n")

c = 3
print(c ** 2)
print("\n")

# División cociente: Nos da el resultado sin redondeo

print(10 / 3)
print("\n")

a = 10
b = 2
print(a / b)
print("\n")

c = 3
print(c / 2)
print("\n")

# División parte entera: Nos da el resultado redondeado

print(10 // 3)
print("\n")

a = 10
b = 3
print(a // b)
print("\n")

c = 3
print(c // 2)
print("\n")

# División resto (o módulo): Nos devuelve el resto de una división, lo que sobra

print(7 % 2)
print("\n")

a = 7
b = 2
print(a % b)
print("\n")

c = 10
print(c % 2)